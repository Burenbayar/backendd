@app.post("/results", response_model=QuizResultOut)
def save_result(
    result_in: QuizResultIn,
    db: Session = Depends(get_db),
    current_user: User = Depends(get_current_user),
):
    quiz_result = QuizResult(
        user_id=current_user.id,
        subject=result_in.subject,
        grade=result_in.grade,
        score=result_in.score,
        total=result_in.total,
        # üîπ –®–ò–ù–≠ –¢–ê–õ–ë–ê–†–£–£–î–´–ì DB-–¥ –•–ê–î–ì–ê–õ–ù–ê
        percentage=result_in.percentage,
        duration_seconds=result_in.duration_seconds,
    )
    db.add(quiz_result)
    db.commit()
    db.refresh(quiz_result)
    return quiz_result


